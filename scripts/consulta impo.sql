SELECT 
R22.NORSRN,
R22.CVPRCN,
RK08.TCMPVP,
R22.NVJES,
R22.CTPOOP,
R22.CVSTR1,
R22.AMNFS,
R22.NMNFEN,
R22.CCMPTR,
R22.CLNNSS,
R22.CPRLLN,
R24.CPRCN3,
R24.NSRCN3,
CONCAT(R24.CPRCN3,R24.NSRCN3) AS CONTENE,
R24.NCNEM1,
R24.NCRCNE,
R24.CTPCR1,
R24.NBLTMN,
R24.PBRKLM,
R24.NBLRCR,
R24.QPBRCR,
R24.SPRPRP,
R25.TTMNCN1,
R25.SESCNI,
R25.SCNRFG,
R25.CTPOC2,
IFNULL(RE07.FINDSC,R22.FLLGNV) AS FINDSC
FROM "DC@RNSLIB".RZIT22 R22
INNER JOIN DC@RNSLIB.RZIT24 R24 ON R22.NORSRN = R24.NORSRN AND R24.SESTRG <>'*'
INNER JOIN DC@RNSLIB.RZIT25 R25 ON R22.NORSRN = R25.NORSRN AND R24.CPRCN3 = R25.CPRCN4 AND R24.NSRCN3 = R25.NSRCN4 AND R25.SESTRG <>'*'
LEFT JOIN DC@RNSLIB.RZET08 RE08 ON R22.NORSRN = RE08.NORSRN 
LEFT JOIN DC@RNSLIB.RZET07 RE07 ON RE08.NMRLQO = RE07.NMRLQO
LEFT JOIN DC@RNSLIB.RZZK08 RK08 ON R22.CVPRCN = RK08.CVPRCN
WHERE R22.SESTRG = 'A'
AND R22.CTPOOP = 1 And R22.NORSRN = 195527 AND r25.TTMNCN1 =20 -- AND R22.FLLGNV BETWEEN 20200216 AND 20200222 
ORDER BY RK08.TCMPVP, R24.NCNEM1, R24.CPRCN3, R24.NSRCN3